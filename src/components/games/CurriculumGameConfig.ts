// Keep the existing functions
export const getGamesBySubject = (subject: string): CurriculumGame[] => {
  return curriculumGames.filter(game => 
    game.subject.toLowerCase() === subject.toLowerCase()
  );
};

export const getGamesByGradeLevel = (gradeLevel: number): CurriculumGame[] => {
  return curriculumGames.filter(game => 
    game.gradeLevel.includes(gradeLevel)
  );
};

export const getGamesBySkillArea = (skillArea: string): CurriculumGame[] => {
  return curriculumGames.filter(game => 
    game.skillAreas.some(skill => 
      skill.toLowerCase().includes(skillArea.toLowerCase())
    )
  );
};

// New helper functions for the game engine
export const getGameById = (gameId: string): CurriculumGame | undefined => {
  return curriculumGames.find(game => game.id === gameId);
};

export const getGamesByInteractionType = (interactionType: string): CurriculumGame[] => {
  return curriculumGames.filter(game => 
    game.interactionType.toLowerCase() === interactionType.toLowerCase()
  );
};

export const getAdaptiveGamesForStudent = (
  subject: string, 
  gradeLevel: number,
  difficulty: number
): CurriculumGame[] => {
  // Start with games matching the subject
  let games = getGamesBySubject(subject);
  
  // Filter by grade level (including one grade below and above for adaptive learning)
  games = games.filter(game => 
    game.gradeLevel.includes(gradeLevel) || 
    game.gradeLevel.includes(gradeLevel - 1) || 
    game.gradeLevel.includes(gradeLevel + 1)
  );
  
  // Map difficulty level to game difficulty
  const difficultyMapping = {
    1: "beginner",
    2: "beginner",
    3: "intermediate",
    4: "intermediate", 
    5: "advanced"
  } as const;
  
  const targetDifficulty = difficultyMapping[difficulty as keyof typeof difficultyMapping] || "intermediate";
  
  // Prioritize games matching the target difficulty, but include others
  const exactDifficultyGames = games.filter(game => game.difficulty === targetDifficulty);
  
  // If we have enough exact matches, return those, otherwise include adjacent difficulties
  if (exactDifficultyGames.length >= 5) {
    return exactDifficultyGames;
  }
  
  return games;
};
